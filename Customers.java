    private void jButtonaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonaddActionPerformed
        if(checkEmptyFields())
        {
            int id = Integer.valueOf(C_id.getText());
            String firstname = C_firstname.getText();
            String lastname = C_lastname.getText();
            String address = C_address.getText();
            int contactno = Integer.valueOf(C_contact.getText());

            String insertQuery = "INSERT INTO `costumer_data`(`Cust_id`, `Cust_Firstname`, `Cust_Lastname`, `Cust_address`, `Contact_no`) VALUES (?,?,?,?,?)";

            try {

                PreparedStatement ps = Connect.getconnection().prepareStatement(insertQuery);
                ps.setInt(1, id);
                ps.setString(2, firstname);
                ps.setString(3, lastname);
                ps.setString(4, address);
                ps.setInt(5, contactno);

                if(ps.executeUpdate() > 0)
                {
                    JOptionPane.showMessageDialog(null, "New Customer Added Successfully", "Add Customer", JOptionPane.INFORMATION_MESSAGE);
                    Customers ct = new Customers();
                    ct.setVisible(true);
                    ct.pack();
                    ct.setLocationRelativeTo(null);
                    this.dispose();
                    System.out.println("New Customer Added");
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Customer Not Added", "Add Customer", JOptionPane.ERROR_MESSAGE);
                    System.out.println("Some Error Message Here");
                }

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null,ex);
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "One Or More Fields Are Empty", "Add Customer", JOptionPane.ERROR_MESSAGE);
            System.out.println("One Or More Fields Are Empty");
        }
    }//GEN-LAST:event_jButtonaddActionPerformed

