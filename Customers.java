    public ArrayList<User> getUserList()
    {
        ArrayList<User> list = new ArrayList<>();
        String query = "SELECT * FROM costumer_data";
        
        PreparedStatement ps;
        ResultSet rs;
        
        try {
            ps = Connect.getconnection().prepareStatement(query);
            rs = ps.executeQuery();
            User user;
            
            while(rs.next())
            {
                user = new User(rs.getInt("Cust_id"), rs.getString("Cust_Firstname"), 
                                      rs.getString("Cust_Lastname"), rs.getString("Cust_address"),
                                      rs.getInt("Contact_no"));
                list.add(user);
                
            }
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,ex);
        }
        
        
        usersArray = list;
        return list;
        
    }
    
    // create a function to show products in table
    public final void showUsersInTable()
    {
        ArrayList<User> usersList = getUserList();
        DefaultTableModel model = (DefaultTableModel) jTable_database.getModel();
        
        // clear jtable
        model.setRowCount(0);
        
        Object[] row = new Object[5];// 5 the number of columns
        
        for(int i = 0; i < usersList.size(); i++)
        {
            row[0] = usersList.get(i).getId();
            row[1] = usersList.get(i).getFirstname();
            row[2] = usersList.get(i).getLastname();
            row[3] = usersList.get(i).getAddress();
            row[4] = usersList.get(i).getContactno();
            
            
            model.addRow(row);
        }
    }
    
